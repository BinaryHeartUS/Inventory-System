#!/usr/bin/env python3
"""Generate fake inventory workbooks for the development environment.

Produces one .xlsx workbook per chapter, each containing the seven sheets the
importer expects (``Desktops``, ``Laptops``, ``Tablets``, ``Ready To Donate``,
``Donated``, ``Parts``, ``Tools``). Column order in every sheet matches the
record-component order the importer reads (see ``importer/src/main/java/...``),
and the first two rows of each sheet are headers (the importer skips them).

The importer imports a whole workbook into a single chapter, so each generated
file targets one chapter. Import them with, e.g.:

    ./scripts/import-data.sh dev <tag> fake-purdue.xlsx "Purdue University"

Device IDs (Desktops/Laptops/Tablets/Ready To Donate) are explicit and must be
globally unique because Asset.ID is a primary key, so a single counter is shared
across every file and device type. Donated devices, parts, and tools get their
IDs auto-generated by the database.

Usage:
    python3 importer/generate_fake_data.py [output_dir]

Requires openpyxl (``pip install openpyxl``). ``output_dir`` defaults to
``importer/data``.
"""

