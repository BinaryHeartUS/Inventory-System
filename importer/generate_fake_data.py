TODAY = dt.date(2026, 7, 22)


def random_date(days_back: int) -> dt.date:
    return TODAY - dt.timedelta(days=random.randint(0, days_back))


def _write_sheet(wb: Workbook, title: str, headers: list[str], rows: list[list]):
    ws = wb.create_sheet(title=title)
    # Two header rows — the importer skips rows 0 and 1 and reads from row 2.
    ws.append([title] + [""] * (len(headers) - 1))
    ws.append(headers)
    for row in rows:
        ws.append(row)
    # Ensure date columns render as dates so Apache POI reads them as dates.
    for row_cells in ws.iter_rows(min_row=3):
        for cell in row_cells:
            if isinstance(cell.value, dt.date):
                cell.number_format = "yyyy-mm-dd"


def _make_workbook(id_counter: list[int]) -> Workbook:
    wb = Workbook()
    # Remove the default sheet created by openpyxl.
    wb.remove(wb.active)

    def next_id() -> int:
        v = id_counter[0]
        id_counter[0] += 1
        return v

    # Desktops
    desktops = []
    for _ in range(COUNTS["desktops"]):
        mfr = random.choice(PC_MANUFACTURERS)
        model = random.choice(DESKTOP_MODELS)
        desktops.append([
            f"{mfr} {model}",
            next_id(),
            mfr,
            random.randint(2012, 2024),
            random.choice(NOTES_POOL),
            random.choice(DEVICE_STATUSES),
            random_date(730),
        ])
    _write_sheet(wb, "Desktops", DESKTOP_HEADERS, desktops)

    # Laptops
    laptops = []
    for _ in range(COUNTS["laptops"]):
        mfr = random.choice(PC_MANUFACTURERS)
        model = random.choice(LAPTOP_MODELS)
        laptops.append([
            f"{mfr} {model}",
            next_id(),
            mfr,
            random.randint(2012, 2024),
            random.choice(NOTES_POOL),
            random.choice(CHARGER_STATUSES),
            random.choice(BATTERY_STATUSES),
            random.choice(DEVICE_STATUSES),
            random_date(730),
        ])
    _write_sheet(wb, "Laptops", LAPTOP_HEADERS, laptops)

    # Tablets
    tablets = []
    for _ in range(COUNTS["tablets"]):
        mfr = random.choice(TABLET_MANUFACTURERS)
        model = random.choice(TABLET_MODELS)
        tablets.append([
            f"{mfr} {model}",
            next_id(),
            mfr,
            random.randint(2013, 2024),
            random.choice(NOTES_POOL),
            random.choice(CHARGER_STATUSES),
            random.choice(BATTERY_STATUSES),
            random.choice(DEVICE_STATUSES),
            random_date(730),
        ])
    _write_sheet(wb, "Tablets", TABLET_HEADERS, tablets)

    # Ready To Donate
    ready = []
    for _ in range(COUNTS["ready_to_donate"]):
        dtype = random.choice(DEVICE_TYPES)
        if dtype == "Tablet":
            mfr = random.choice(TABLET_MANUFACTURERS)
            model = random.choice(TABLET_MODELS)
            os_val = random.choice(TABLET_OS)
        else:
            mfr = random.choice(PC_MANUFACTURERS)
            model = random.choice(DESKTOP_MODELS if dtype == "Desktop" else LAPTOP_MODELS)
            os_val = random.choice(PC_OS)
        ready.append([
            f"{mfr} {model}",
            mfr,
            dtype,
            random.randint(2014, 2024),
            random.choice(CPUS),
            os_val,
            random.choice(RAM_AMOUNTS),
            random.choice(RAM_GENERATIONS),
            random.choice(STORAGE_CAPACITIES),
            random.choice(STORAGE_TYPES),
            random.randint(40, 450),
            random.choice(NOTES_POOL),
            next_id(),
        ])
    _write_sheet(wb, "Ready To Donate", READY_HEADERS, ready)

    # Donated (IDs auto-generated by the DB)
    donated = []
    for _ in range(COUNTS["donated"]):
        dtype = random.choice(DEVICE_TYPES)
        if dtype == "Tablet":
            mfr = random.choice(TABLET_MANUFACTURERS)
            model = random.choice(TABLET_MODELS)
            os_val = random.choice(TABLET_OS)
        else:
            mfr = random.choice(PC_MANUFACTURERS)
            model = random.choice(DESKTOP_MODELS if dtype == "Desktop" else LAPTOP_MODELS)
            os_val = random.choice(PC_OS)
        is_org = random.random() < 0.6
        recipient = random.choice(RECIPIENT_ORGS if is_org else RECIPIENT_PEOPLE)
        donated.append([
            f"{mfr} {model}",
            mfr,
            dtype,
            random.randint(2014, 2024),
            random.choice(CPUS),
            os_val,
            random.choice(RAM_AMOUNTS),
            random.choice(RAM_GENERATIONS),
            random.choice(STORAGE_CAPACITIES),
            random.choice(STORAGE_TYPES),
            random.randint(40, 450),
            random.choice(NOTES_POOL),
            random_date(365),
            recipient,
            "Y" if is_org else "N",
        ])
    _write_sheet(wb, "Donated", DONATED_HEADERS, donated)

    # Parts (quantity expands into individual parts on import)
    parts = []
    for _ in range(COUNTS["parts_rows"]):
        parts.append([
            random.choice(PART_TYPES),
            random.choice(PART_DESCRIPTIONS),
            random.randint(1, 8),
            random.choice(["Y", "N"]),
            round(random.uniform(2.0, 120.0), 2),
        ])
    _write_sheet(wb, "Parts", PART_HEADERS, parts)

    # Tools (quantity expands into individual tools on import)
    tools = []
    for _ in range(COUNTS["tools_rows"]):
        tools.append([
            random.choice(TOOL_DESCRIPTIONS),
            random.randint(1, 5),
            round(random.uniform(3.0, 200.0), 2),
        ])
    _write_sheet(wb, "Tools", TOOL_HEADERS, tools)

    return wb


def main() -> None:
    out_dir = sys.argv[1] if len(sys.argv) > 1 else os.path.join(
        os.path.dirname(os.path.abspath(__file__)), "data"
    )
    os.makedirs(out_dir, exist_ok=True)
    random.seed(RANDOM_SEED)

    # Shared, ever-increasing device-ID counter across all workbooks.
    id_counter = [ID_START]

    for filename, chapter_name in CHAPTERS:
        wb = _make_workbook(id_counter)
        path = os.path.join(out_dir, filename)
        wb.save(path)
        print(f"Wrote {path}  ->  chapter \"{chapter_name}\"")

    print(f"\nHighest device ID used: {id_counter[0] - 1}")
    print("Import each file with its chapter name, e.g.:")
    for filename, chapter_name in CHAPTERS:
        print(f'  ./scripts/import-data.sh dev <tag> {filename} "{chapter_name}"')


if __name__ == "__main__":
    main()
